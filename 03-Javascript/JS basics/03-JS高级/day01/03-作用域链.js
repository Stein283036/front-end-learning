/* 作用域链
作用域链就是变量的查找顺序机制,在函数被执行时,会优先在当前函数作用域查找变量,如果
有则不会向上查找父级作用域直到全局作用域

嵌套关系的作用域串联起来形成了作用域链
子作用域能够访问父作用域，父级作用域无法访问子级作用域(就近原则)
 */

// 全局作用域
let a = 1;
let b = 2;

// 局部作用域
function f() {
  let a = 10;
  console.log(a); // 1 或 10
  // 父级作用域不会向子级作用域查找变量
  //   console.log(d); // 报错

  // 局部作用域
  function g() {
    let d = "yo";
    let b = 20;
    console.log(b); // 2 或 20
  }

  // 调用 g 函数
  g();
}

f();
